export const useTranslation = () => {
  const [lang, setLang] = useState(localStorage.getItem("appLang") || "en");

  const updateAppLanguage = (newLang: string) => {
    setLang(newLang);
    document.documentElement.lang = newLang;
    document.documentElement.dir = newLang === 'ur' ? 'rtl' : 'ltr';
    
    // Add a class to body for RTL specific styling if needed
    if (newLang === 'ur') {
      document.body.classList.add('rtl');
    } else {
      document.body.classList.remove('rtl');
    }
  };

  useEffect(() => {
    // Initial setup
    updateAppLanguage(lang);

    const handleStorageChange = () => {
      const newLang = localStorage.getItem("appLang") || "en";
      updateAppLanguage(newLang);
    };

    window.addEventListener("storage", handleStorageChange);
    window.addEventListener("lang-change", handleStorageChange);

    return () => {
      window.removeEventListener("storage", handleStorageChange);
      window.removeEventListener("lang-change", handleStorageChange);
    };
  }, [lang]);

  const t = (text: string) => {
    if (translations[lang] && translations[lang][text]) {
      return translations[lang][text];
    }
    return text; // fallback to key itself
  };

  return { t, lang };
};
